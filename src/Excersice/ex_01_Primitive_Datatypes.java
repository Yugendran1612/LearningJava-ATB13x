package Excersice;

public class ex_01_Primitive_Datatypes {
    public static void main(String[] args) {
        int A = 10;
        float B = 3.64f;
        double C = 34.3435d;
        long D = 657436578l;
        char E = 'A';
        short F = 435;
        boolean Javaisoutdated = false;
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
        System.out.println(D);
        System.out.println(E);
        System.out.println(F);
        System.out.println(Javaisoutdated);
    }
}
