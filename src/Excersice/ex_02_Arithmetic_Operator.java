package Excersice;

public class ex_02_Arithmetic_Operator {
    public static void main(String[] args){
        int a = 10;
        int b = 5;
        int add = a+b;
        System.out.println("The addition value is "+ add);
        int sub = a-b;
        System.out.println("The substraction value is "+ sub);
        int mul = a*b;
        System.out.println("The multiplication value is "+ mul);
        int div = a/b;
        System.out.println("The division value is "+ div);
        int mod = a%b;
        System.out.println("The modules value is "+ mod);
    }
}
